# --------
# for loop
# --------

for x in range(1,10): #prints 1 to 9
    print(x)

for letter in "coffee":
    print(letter)

# -------
# range()
# -------

# range(6) -> 0,1,2,3,4,5
# range(1,7) -> 1,2,3,4,5,6
# range(1,10,2) -> 1,3,5,7,9
# range(7,0,-1) -> 7,6,5,4,3,2,1

# ----------
# while loop
# ----------

num = 1
while num < 11:
    print(num)
    num += 1